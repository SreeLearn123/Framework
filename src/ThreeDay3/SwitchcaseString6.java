package ThreeDay3;

public class SwitchcaseString6 {

	public static void main(String[] args) {
	    String weekday="saturday";
		switch(weekday)
		{
		case "sunday": System.out.println("day1");
		break;
		case "monday": System.out.println("day2");
		break;
		case "Tuesday": System.out.println("day3");
		break;
		case "wednusday": System.out.println("day4");
		break;
		case "Thursday": System.out.println("day5");
		break;
		
		case "friday": System.out.println("day6");
		break;
		case "saturday": System.out.println("day7");
		break;
		default:  System.out.println("Invalid day");
		}

	}

}
